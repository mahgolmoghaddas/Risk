package com.riskgame.model;
/**
 * This specifies the x and y coordinates for the territory
 * @author gauta
 *
 */
public class Coordinates {

	private double x;
	private double y;

	public Coordinates(double x, double y) {
		this.x = x;
		this.y = y;
	}

	public double getX() {
		return x;
	}

	public double getY() {
		return y;
	}

}
